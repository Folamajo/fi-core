CREATE OR REPLACE FUNCTION "public"."create_profile"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$BEGIN 
  INSERT INTO public.profiles(user_id)
  VALUES(NEW.id);

  return NEW;
end;$$;



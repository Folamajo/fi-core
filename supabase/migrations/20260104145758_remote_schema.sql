CREATE OR REPLACE FUNCTION "public"."create_project"("project_name" "text", "feedback_items" "text"[]) RETURNS TABLE("project_id" integer, "analysis_id" integer, "feedback_count" integer)
    LANGUAGE "plpgsql"
    AS $$
DECLARE

  user_id uuid;

BEGIN
  user_id := auth.uid();
  project_name := project_name || '-' || current_timestamp;

  INSERT INTO projects(project_name, user_id)
  VALUES (project_name, user_id)
  RETURNING id INTO project_id;

  INSERT INTO analyses(status, project_id)
  VALUES('pending', project_id)
  RETURNING id INTO analysis_id;

  INSERT INTO feedback_items(feedback_item, analysis_id)
  SELECT unnest(feedback_items), analysis_id;

  GET DIAGNOSTICS feedback_count = ROW_COUNT;

  RETURN;
END
$$;


